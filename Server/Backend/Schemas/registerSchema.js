const mongoose = require("mongoose");
const registrationSchema = new mongoose.Schema({
  username: {
    type: String,
    required: true,
    unique: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
    minlength: 8,
  },
  role: {
    type: String,
    required: true,
  },
});

const Registration = mongoose.model("registration", registrationSchema);

module.exports = Registration;
